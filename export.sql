pr_for_one INT;
da timestamp;
    after each row is
    begin
      size_range_shoes_id:= :new.shoes_id;
      size_range_quan:= :new.quantity_in_package;
    end
    after each row;
    after statement is
    begin
    select max(shoes_in_package) into all_shoes_in_pack from quantity_package_change where end_date is null and shoes_id = size_range_shoes_id;
    update quantity_package_change
           set end_date = sysdate - 1/84600
           where end_date is null and shoes_id = size_range_shoes_id;
    if all_shoes_in_pack is null then all_shoes_in_pack :=0; end if;
    insert into quantity_package_change values(size_range_shoes_id, sysdate, null, all_shoes_in_pack+size_range_quan);
    select price_for_one into pr_for_one from sell_price_change where shoes_id = size_range_shoes_id and end_date is null;
    update sell_price_change set end_date = sysdate-1/84600 where shoes_id = size_range_shoes_id and end_date is null;
    insert into sell_price_change values(size_range_shoes_id, sysdate, null, pr_for_one, pr_for_one*(all_shoes_in_pack+size_range_quan));
    end
    after statement;
    end;

